    {
        //
        $credentials = $request->only('email','password');
        if(Auth::attempt($credentials)){
            return response([
                'message'=>'Authentication Success',
                'token'=>Auth::user()->createToken('email')->plainTextToken
            ]);
        }else{
            return response([
                'message'=>'Authentication Failed'
            ],401);
